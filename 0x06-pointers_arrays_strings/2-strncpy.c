/**
* _strncat - Copy strings
* @dest: Destination
* @src: Source
* @n: Size of string
*
* Return: Mutated string @dest
*/
char *_strncpy(char *dest, char *src, int n)
{
	int i;

	for (i = 0; i < n && src[i] != '\0'; i++)
	{
		dest[i] = src[i];
	}
	for ( ; i < n; i++)
	{
		dest[i] = '\0';
	}
	return dest;
}
