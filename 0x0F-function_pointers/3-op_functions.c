#include "3-calc.h"
/**
 * op_sub - Subtract ints
 * @a: First int
 * @b: Second int
 * Return: Answer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_add - Add ints
 * @a: First int
 * @b: Second int
 * Return: Answer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_mul - Multiply ints
 * @a: First int
 * @b: Second int
 * Return: Answer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Divide ints
 * @a: First int
 * @b: Second int
 * Return: Answer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Modulo of two ints
 * @a: First int
 * @b: Second int
 * Return: Answer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
